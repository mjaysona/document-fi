export async function getTransactionsPage(
  params: GetTransactionsPageParams = {},
): Promise<TransactionPageResult> {
  try {
    const payload = await getPayload({ config })

    const page = Number.isInteger(params.page) && Number(params.page) > 0 ? Number(params.page) : 1
    const pageSize =
      Number.isInteger(params.pageSize) && Number(params.pageSize) > 0
        ? Number(params.pageSize)
        : 10

    const sortBy = params.sortBy || 'date'
    const sortOrder = params.sortOrder || 'desc'
    const sortField =
      sortBy === 'amount' ? 'amount' : sortBy === 'updated' ? 'lastUpdated' : 'transactionDate'
    const sort = `${sortOrder === 'asc' ? '' : '-'}${sortField}`

    const whereAnd: Record<string, unknown>[] = []

    const normalizedSearch = String(params.search || '').trim()
    if (normalizedSearch) {
      const uppercaseSearch = normalizedSearch.toUpperCase()
      const escapedSearch = escapeRegexLikeValue(normalizedSearch)
      whereAnd.push({
        or: [
          { referenceNumber: { equals: normalizedSearch } },
          { referenceNumber: { equals: uppercaseSearch } },
          { referenceNumber: { like: escapedSearch } },
        ],
      })
    }

    const financialAccountId = String(params.financialAccountId || '').trim()
    if (financialAccountId) {
      whereAnd.push({
        financialAccount: {
          equals: financialAccountId,
        },
      })
    }

    const typeFilters = (params.transactionTypes || []).filter(
      (value): value is TransactionType => value === 'debit' || value === 'credit',
    )
    if (typeFilters.length > 0) {
      whereAnd.push({
        transactionType: {
          in: typeFilters,
        },
      })
    }

    const statusFilters = (params.transactionStatuses || []).filter(
      (value): value is TransactionStatus => value === 'completed' || value === 'failed',
    )
    if (statusFilters.length > 0) {
      whereAnd.push({
        transactionStatus: {
          in: statusFilters,
        },
      })
    }

    const sourceAccountIds = Array.from(
      new Set(
        (params.sourceAccountIds || []).map((value) => String(value || '').trim()).filter(Boolean),
      ),
    )
    if (sourceAccountIds.length > 0) {
      whereAnd.push({
        sourceAccount: {
          in: sourceAccountIds,
        },
      })
    }

    const destinationAccountIds = Array.from(
      new Set(
        (params.destinationAccountIds || [])
          .map((value) => String(value || '').trim())
          .filter(Boolean),
      ),
    )
    if (destinationAccountIds.length > 0) {
      whereAnd.push({
        destinationAccount: {
          in: destinationAccountIds,
        },
      })
    }

    const normalizedStartDate = normalizeTransactionDate(params.startDate, { boundary: 'start' })
    if (normalizedStartDate) {
      whereAnd.push({
        transactionDate: {
          greater_than_equal: normalizedStartDate,
        },
      })
    }

    const normalizedEndDate = normalizeTransactionDate(params.endDate, { boundary: 'end' })
    if (normalizedEndDate) {
      whereAnd.push({
        transactionDate: {
          less_than_equal: normalizedEndDate,
        },
      })
    }

    const result = await payload.find({
      collection: 'transactions',
      sort,
      page,
      limit: pageSize,
      depth: 1,
      ...(whereAnd.length > 0
        ? {
            where: {
              and: whereAnd,
            } as any,
          }
        : {}),
    })

    return {
      success: true,
      data: (result.docs as any[]).map(mapTransactionDocToListItem),
      pagination: {
        page: Number(result.page || page),
        pageSize,
        totalDocs: Number(result.totalDocs || 0),
        totalPages: Number(result.totalPages || 1),
      },
    }
  } catch (error) {
    console.error('Failed to fetch transaction page:', error)
    return { success: false, data: [], error: 'Failed to load transactions.' }
  }
}

