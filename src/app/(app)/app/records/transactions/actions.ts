export async function updateTransaction(
  id: string,
  input: TransactionFormInput,
): Promise<{ success: boolean; error?: string; existingTransactionId?: string }> {
  try {
    const session = await auth.api.getSession({ headers: await headers() })
    if (!session?.user?.id) return { success: false, error: 'Unauthorized' }

    const description = input.description.trim()
    const transactionType = normalizeTransactionType(input.transactionType)
    const amount = normalizeAmount(input.amount)
    const transactionFee = normalizeNonNegativeAmount(input.transactionFee)
    const financialAccount = String(input.financialAccount || '').trim()
    const parentTransaction = String(input.parentTransaction || '').trim()
    const providedSourceAccount = String(input.sourceAccount || '').trim()

    if (!transactionType) return { success: false, error: 'Transaction type is required.' }
    if (!amount) return { success: false, error: 'Amount must be greater than zero.' }
    if (!financialAccount && !parentTransaction) {
      return { success: false, error: 'Financial account is required.' }
    }

    let resolvedSourceAccount = providedSourceAccount || undefined
    if (!resolvedSourceAccount) {
      return { success: false, error: 'Source bank is required.' }
    }

    const payload = await getPayload({ config })
    const currentDoc = (await payload.findByID({
      collection: 'transactions',
      id,
      depth: 0,
      overrideAccess: true,
    })) as { referenceNumber?: unknown; isAutoGenerated?: unknown }

    const currentReferenceNumber = normalizeReferenceNumber(currentDoc.referenceNumber)
    const incomingReferenceNumber = normalizeReferenceNumber(input.referenceNumber)
    let resolvedReferenceNumber = incomingReferenceNumber
    let isAutoGenerated = false

    if (!resolvedReferenceNumber) {
      resolvedReferenceNumber = await generateUniqueAutoReferenceNumber({
        payload,
        transactionDate: new Date().toISOString(),
        excludeTransactionId: id,
      })
      isAutoGenerated = true
    } else if (
      currentDoc.isAutoGenerated === true &&
      currentReferenceNumber &&
      resolvedReferenceNumber === currentReferenceNumber
    ) {
      isAutoGenerated = true
    }

    const existingTransactionId = await isReferenceNumberTaken({
      payload,
      referenceNumber: resolvedReferenceNumber,
      excludeTransactionId: id,
    })

    if (existingTransactionId) {
      return {
        success: false,
        error: 'Reference number already exists.',
        existingTransactionId,
      }
    }

    await payload.update({
      collection: 'transactions',
      id,
      data: mapTransactionInput({
        ...input,
        referenceNumber: resolvedReferenceNumber,
        isAutoGenerated,
        sourceAccount: resolvedSourceAccount,
        description,
        transactionType,
        amount,
        transactionFee,
        ...(financialAccount ? { financialAccount } : {}),
        ...(typeof input.allocatedFundType !== 'undefined'
          ? { allocatedFundType: input.allocatedFundType }
          : {}),
      }),
      depth: 0,
    })

    return { success: true }
  } catch (error) {
    console.error('Failed to update transaction:', error)
    return { success: false, error: 'Failed to update transaction.' }
  }
}

export async function updateTransactionWithReceipt(
  id: string,
  formData: FormData,
): Promise<{ success: boolean; error?: string; existingTransactionId?: string }> {
  try {
    const session = await auth.api.getSession({ headers: await headers() })
    if (!session?.user?.id) return { success: false, error: 'Unauthorized' }

    const payload = await getPayload({ config })
    const currentTx = await payload.findByID({
      collection: 'transactions',
      id,
      depth: 0,
    })

    const existingReceiptImageId = (currentTx as any).receiptImage
      ? typeof (currentTx as any).receiptImage === 'string'
        ? String((currentTx as any).receiptImage)
        : String((currentTx as any).receiptImage.id)
      : undefined

    let receiptImageId = String(formData.get('existingReceiptImageId') || '').trim() || undefined
    let rawOcrText = String(formData.get('rawOcrText') || '').trim() || undefined

    const nextReceipt = formData.get('file')
    if (nextReceipt instanceof File && nextReceipt.size > 0) {
      const receiptResult = await createTransactionReceiptFromFormData(formData)
      if (!receiptResult.success || !receiptResult.id) {
        return { success: false, error: receiptResult.error ?? 'Failed to save receipt.' }
      }

      if (existingReceiptImageId) {
        await deleteTransactionReceiptById(existingReceiptImageId)
      }

      receiptImageId = receiptResult.id
      rawOcrText = receiptResult.rawOcrText
    } else if (!receiptImageId && existingReceiptImageId) {
      await deleteTransactionReceiptById(existingReceiptImageId)
    }

    const financialAccount = String(formData.get('financialAccount') || '').trim() || undefined
    const aiExtractedJson = parseAiExtractedJsonValue(formData.get('aiExtractedJson'))
    const extractionConfidence = parseConfidenceValue(formData.get('extractionConfidence'))
    const isAllocatedFund = String(formData.get('isAllocatedFund') || '').trim() === 'true'
    const isForAllocation = String(formData.get('isForAllocation') || '').trim() === 'true'
    const parentTransaction = isAllocatedFund
      ? String(formData.get('parentTransaction') || '').trim() || null
      : null
    // Only allow valid values for allocatedFundType
    const allocatedFundTypeRaw = String(formData.get('allocatedFundType') || '').trim()
    const allocatedFundType: 'completed' | 'returned' | null | undefined =
      allocatedFundTypeRaw === 'completed' || allocatedFundTypeRaw === 'returned'
        ? allocatedFundTypeRaw
        : undefined
    return await updateTransaction(id, {
      transactionDate: String(formData.get('transactionDate') || '').trim() || undefined,
      description: String(formData.get('description') || '').trim(),
      particulars: String(formData.get('particulars') || '').trim() || undefined,
      transactionType: normalizeTransactionType(formData.get('transactionType')),
      sourceAccount: String(formData.get('sourceAccount') || '').trim() || undefined,
      destinationAccount: String(formData.get('destinationAccount') || '').trim() || undefined,
      financialAccount,
      from: String(formData.get('from') || '').trim() || undefined,
      to: String(formData.get('to') || '').trim() || undefined,
      referenceNumber: String(formData.get('referenceNumber') || '').trim() || undefined,
      isAutoGenerated: String(formData.get('isAutoGenerated') || '').trim() === 'true',
      amount: normalizeAmount(formData.get('amount')),
      transactionFee: normalizeNonNegativeAmount(formData.get('transactionFee')),
      currentBalance: normalizeNonNegativeAmount(formData.get('currentBalance')),
      transactionStatus: normalizeTransactionStatus(formData.get('transactionStatus')),
      receiptImageId,
      rawOcrText,
      aiExtractedJson,
      extractionConfidence,
      isAllocatedFund,
      isForAllocation,
      parentTransaction,
      allocatedFundType,
    })
  } catch (error) {
    console.error('Failed to update transaction with receipt:', error)
    return { success: false, error: 'Failed to update transaction.' }
  }
}

